public class Pj {
	public Punto punto;
	public float ancho;
	public float alto;
	public float anchoCanon;
	public float altoCanon;
	public float velocidade;
	public float carga;
	
	public Pj(Punto punto,float ancho,float alto,float anchoCanon,float altoCanon,float velocidade,float carga) {
		this.punto = punto;
		this.ancho = ancho;
		this.alto = alto;
		this.anchoCanon = anchoCanon;
		this.altoCanon = altoCanon;
		this.velocidade = velocidade;
		this.carga = carga;
	}
}
